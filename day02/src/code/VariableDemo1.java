package code;

public class VariableDemo1 {
    public static void main(String[] args) {
        //定义变量
        //数据类型 变量名 = 值;
        //数据类型：限定变量能存储的类型
        //int 整数 double整数
        //等号：赋值。右边的数据赋值给左边的变量

        int a = 10;
        System.out.println(a);

        //一条语句可以定义多个变量
        int b = 10 , c = 20;
        System.out.println(b);
        System.out.println(c);
    }
}
