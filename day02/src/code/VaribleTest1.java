package code;

public class VaribleTest1 {
    public static void main(String[] args) {
        //记录姓名
        String name = "张三";
        System.out.println(name);

        //记录年龄
        int age = 18;
        System.out.println(age);

        //记录性别
        char gender = '男';
        System.out.println(gender);

        //记录身高
        double height = 1.88;
        System.out.println(height);

        //记录体重
        float weight = 80.5f;
        System.out.println(weight);

        //记录是否合格
        boolean isPass = true;

    }
}
