package code;

public class ArithmeticoperatorDemo1 {
    public static void main(String[] args) {
        //整数参与运算，结果只能的得到整数
        //小数参与计算，结果可能是不准确的

        //除法
        System.out.println(10 / 2);
        System.out.println(10 / 3);
        System.out.println(10.0 / 3);

        //取余数
        System.out.println(10 % 2);
        System.out.println(10 % 3);

        //应用场景
        //1.通过取模判断，A是否被B整除
        //2.判断A是否为偶数
        //斗地主发牌
    }
}
